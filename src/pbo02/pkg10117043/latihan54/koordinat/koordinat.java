package pbo02.pkg10117043.latihan54.koordinat;

/**
 *
 * @author Acer
 */
public class koordinat {

    protected int x;
    protected int y;

    public koordinat(int x, int y) {
        this.x = x;
        this.y = y;
     }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
